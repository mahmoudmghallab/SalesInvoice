    public int getNextInvoiceNum() {
        int num = 0;
        
        for (InvoiceHeader invoice : getInvoices()) {
            if (invoice.getInvoiceNum()> num) 
                num = invoice.getInvoiceNum();
        }
        
        return ++num;
    }
    
    
    
